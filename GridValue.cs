

namespace SnakeGame
{
    /*This code defines an enumeration named GridValue which represents 
       the possible values that can be present in a cell of the Snake game grid. 
        The enumeration has four possible values:

        Empty: represents an empty cell in the grid
        Snake: represents a cell containing a part of the snake's body
        Food: represents a cell containing a food item that the snake can eat
        Outside: represents a cell that is outside the boundaries of the grid*/
    public enum GridValue
    {
        Empty,
        Snake,
        Food,
        Outside

    }
}
